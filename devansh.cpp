#include<stdio.h>

int main(){
    // FOR LOOP
int i;
for ( i = 0; i <7; i++)
{
printf("Merry Christmas !! Happy New Year 2023\n");
}
printf("\n\n\n");

// WHILE LOOP
int j=1;
while (j<8)
{
  printf("Merry Christmas !! Happy New Year 2023\n");
  j++;
}
printf("\n\n\n");
//DO-WHILE LOOP

int k=0;
do
{
 printf("Merry Christmas !! Happy New Year 2023\n");
 k++;
} while (k<7);

return 0;
}
